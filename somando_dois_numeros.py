#  Crie um programa que leia dois numeros e mostre a soma entre eles.

primeiro_numero = int(input('Digite o primeiro numero: '))
segundo_numero = int(input('Digite o segundo numero: '))
soma: int = primeiro_numero + segundo_numero
# print('A soma entre', primeiro_numero, 'e', segundo_numero, 'vale:', soma)
print(f'A soma entre {primeiro_numero} e {segundo_numero} vale {soma} ')
